SELECT
    COALESCE(CS_NET_PAID_INC_SHIP_TAX, 0) AS NET_PAID_INC_SHIP_TAX,
    COALESCE(CS_LIST_PRICE, 0) AS LIST_PRICE,
    CAST(CS_SHIP_HDEMO_SK AS VARCHAR) AS SHIP_HDEMO_SK,
    CAST(CS_SHIP_CUSTOMER_SK AS VARCHAR) AS SHIP_CUSTOMER_SK,
    CAST(CS_CATALOG_PAGE_SK AS VARCHAR) AS CATALOG_PAGE_SK,
    CAST(CS_SHIP_ADDR_SK AS VARCHAR) AS SHIP_ADDR_SK,
    CAST(CS_SHIP_DATE_SK AS VARCHAR) AS SHIP_DATE_SK,
    CS_ORDER_NUMBER AS ORDER_NUMBER,
    COALESCE(CS_COUPON_AMT, 0) AS COUPON_AMT,
    COALESCE(CS_EXT_DISCOUNT_AMT, 0) AS EXT_DISCOUNT_AMT,
    CAST(CS_BILL_CUSTOMER_SK AS VARCHAR) AS BILL_CUSTOMER_SK,
    CAST(CS_SHIP_MODE_SK AS VARCHAR) AS SHIP_MODE_SK,
    COALESCE(CS_SALES_PRICE, 0) AS SALES_PRICE,
    CAST(CS_WAREHOUSE_SK AS VARCHAR) AS WAREHOUSE_SK,
    CAST(CS_SHIP_CDEMO_SK AS VARCHAR) AS SHIP_CDEMO_SK,
    COALESCE(CS_EXT_WHOLESALE_COST, 0) AS EXT_WHOLESALE_COST,
    COALESCE(CS_WHOLESALE_COST, 0) AS WHOLESALE_COST,
    CAST(CS_SOLD_TIME_SK AS VARCHAR) AS SOLD_TIME_SK,
    COALESCE(CS_QUANTITY, 0) AS QUANTITY,
    COALESCE(CS_EXT_TAX, 0) AS EXT_TAX,
    CAST(CS_ITEM_SK AS VARCHAR) AS ITEM_SK,
    CAST(CS_CALL_CENTER_SK AS VARCHAR) AS CALL_CENTER_SK,
    CAST(CS_BILL_CDEMO_SK AS VARCHAR) AS BILL_CDEMO_SK,
    CAST(CS_PROMO_SK AS VARCHAR) AS PROMO_SK,
    COALESCE(CS_NET_PAID_INC_TAX, 0) AS NET_PAID_INC_TAX,
    COALESCE(CS_NET_PROFIT, 0) AS NET_PROFIT,
    COALESCE(CS_NET_PAID, 0) AS NET_PAID,
    CAST(CS_SOLD_DATE_SK AS VARCHAR) AS SOLD_DATE_SK,
    COALESCE(CS_EXT_SALES_PRICE, 0) AS EXT_SALES_PRICE,
    COALESCE(CS_EXT_SHIP_COST, 0) AS EXT_SHIP_COST,
    COALESCE(CS_EXT_LIST_PRICE, 0) AS EXT_LIST_PRICE,
    CAST(CS_BILL_ADDR_SK AS VARCHAR) AS BILL_ADDR_SK,
    CAST(CS_BILL_HDEMO_SK AS VARCHAR) AS BILL_HDEMO_SK,
    COALESCE(CS_NET_PAID_INC_SHIP, 0) AS NET_PAID_INC_SHIP
FROM {{ source('raw', 'catalog_sales') }}

